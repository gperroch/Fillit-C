#include "fillit.h"

void		ft_lens(char *buf, char *lens)
{
	int		i;
	int		j;
	int		l;

	i = 0;
	j = 0;
	l = 0;
	while (i < 20)
	{
		if (buf[i] == '#')
		{
			lens[j] = lens[j] + 1;
			l++;
		}
		else if (buf[i] == '\n' && l != 0)
			j++;
		i++;
	}
}
