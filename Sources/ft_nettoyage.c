#include "fillit.h"

void		ft_nettoyage(char **map)
{
	int	i;
	int	j;

	j = 0;
	while (j < 26)
	{
		i = 0;
		while (i < 26)
		{
			map[j][i] = '.';
			i++;
		}
		j++;
	}
}
