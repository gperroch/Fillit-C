#include "fillit.h"

int		ft_check_char(char *buf, int i)
{
	if (buf[i] != '.' && buf[i] != '#' && buf[i] != '\n')
		return (-1);
	else if (buf[i] == '#' && buf[i - 1] != '#' && buf[i + 1] != '#' &&
			buf[i - 5] != '#' && buf[i + 5] != '#')
		return (-1);
	return (0);
}
